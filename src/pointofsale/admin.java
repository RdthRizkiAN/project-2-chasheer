    private void Level(){ 
        if (label_level.getText().equals("Admin")){
            button_databarang.setEnabled(true);
            button_datatransaksi.setEnabled(true);
            button_transaksi.setEnabled(true);
            button_datasupplier.setEnabled(true);
            button_datapengguna.setEnabled(true);
            button_databarangmasuk.setEnabled(true);
            button_databarangkeluar.setEnabled(true);
            button_opsi.setEnabled(true); 

        }
        else if (label_level.getText().equals("Gudang")) {
            button_databarang.setEnabled(true);
            button_datatransaksi.setEnabled(false);
            button_transaksi.setEnabled(false);
            button_datasupplier.setEnabled(false);
            button_datapengguna.setEnabled(false);
            button_databarangmasuk.setEnabled(false);
            button_databarangkeluar.setEnabled(false);
            button_opsi.setEnabled(false); 
        }
        else if (label_level.getText().equals("Kasir")) {
            button_databarang.setEnabled(false);
            button_datatransaksi.setEnabled(false);
            button_transaksi.setEnabled(true);
            button_datasupplier.setEnabled(false);
            button_datapengguna.setEnabled(false);
            button_databarangmasuk.setEnabled(false);
            button_databarangkeluar.setEnabled(false);
            button_opsi.setEnabled(false); 
        }
    }

